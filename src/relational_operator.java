//relational operators in JAVA
public class relational_operator {
    public static void main(String[] args) {
        int p=6;
        int q=8;
        System.out.println("p==q="+(p==q));
        System.out.println("p!=q="+(p!=q));
        System.out.println("p>q="+(p>q));
        System.out.println(("p<q="+(p<q)));
        System.out.println("p>=q="+(p>=q));
        System.out.println("p<=q="+(p<=q));
    }
    
}
